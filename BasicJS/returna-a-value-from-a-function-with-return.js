//Создайте функцию timesFive, которая принимает один аргумент, умножает его на 5 и возвращает новое значение.

// ✅ SOLUTION:

function timesFive(num1) {
    return num1 * 5;
}
const five = timesFive(5);
const two = timesFive(2);
const zero = timesFive(0);

console.log(five);
console.log(two);
console.log(zero);